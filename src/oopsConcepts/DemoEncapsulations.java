package oopsConcepts;

public class DemoEncapsulations
{

	private String name;
	private int Age;
	
	
	public String getName()
	{
		return name;
	}
	public void setName(String name)
	{
		this.name = name;
	}
	
	
	
	
	public int getAge()
	{
		return Age;
	}
	
	public void setAge(int age) 
	{
		Age = age;
	}
	
	
}
